package com.ivucenter.cloud.api.openstack.object;

public class BlockDeviceStorageMapping {
	private String boot_index;
	private String uuid;
	private String source_type;
	private String volume_size;
	private String destination_type;
	private Boolean delete_on_termination;
	private String tag;

	public String getBoot_index() {
		return boot_index;
	}

	public void setBoot_index(String boot_index) {
		this.boot_index = boot_index;
	}

	public String getUuid() {
		return uuid;
	}

	public void setUuid(String uuid) {
		this.uuid = uuid;
	}

	public String getSource_type() {
		return source_type;
	}

	public void setSource_type(String source_type) {
		this.source_type = source_type;
	}

	public String getVolume_size() {
		return volume_size;
	}

	public void setVolume_size(String volume_size) {
		this.volume_size = volume_size;
	}

	public String getDestination_type() {
		return destination_type;
	}

	public void setDestination_type(String destination_type) {
		this.destination_type = destination_type;
	}

	public Boolean getDelete_on_termination() {
		return delete_on_termination;
	}

	public void setDelete_on_termination(Boolean delete_on_termination) {
		this.delete_on_termination = delete_on_termination;
	}

	public String getTag() {
		return tag;
	}

	public void setTag(String tag) {
		this.tag = tag;
	}

}
