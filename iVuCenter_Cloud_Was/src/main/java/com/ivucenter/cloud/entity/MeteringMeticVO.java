package com.ivucenter.cloud.entity;

import java.util.Date;

public class MeteringMeticVO extends DataTableVO{
	private String metricId;
	private String metricItem;
	private String resourceId;
	private Date regDatetime;
	private String serviceType;
	
	public String getServiceType() {
		return serviceType;
	}
	public void setServiceType(String serviceType) {
		this.serviceType = serviceType;
	}
	public String getMetricId() {
		return metricId;
	}
	public void setMetricId(String metricId) {
		this.metricId = metricId;
	}
	public String getMetricItem() {
		return metricItem;
	}
	public void setMetricItem(String metricItem) {
		this.metricItem = metricItem;
	}
	public String getResourceId() {
		return resourceId;
	}
	public void setResourceId(String resourceId) {
		this.resourceId = resourceId;
	}
	public Date getRegDatetime() {
		return regDatetime;
	}
	public void setRegDatetime(Date regDatetime) {
		this.regDatetime = regDatetime;
	}
	
	@Override
	public String toString() {
		return "MetringMeticVO [metricId=" + metricId + ", metricItem=" + metricItem + ", resourceId=" + resourceId
				+ ", regDatetime=" + regDatetime + "]";
	}
	
	
}
