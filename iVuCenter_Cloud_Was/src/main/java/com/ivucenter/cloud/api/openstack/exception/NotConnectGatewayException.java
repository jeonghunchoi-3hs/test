package com.ivucenter.cloud.api.openstack.exception;

public class NotConnectGatewayException extends Exception {
	private static final long serialVersionUID = 2191630284423776795L;

	private int code;

	public int getCode() {
		return code;
	}

	public NotConnectGatewayException(int code, String message) {
		super(message);
		this.code = code;
	}
}
